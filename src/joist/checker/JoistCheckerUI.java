    private boolean checkAllInputs() {
        return (checkIndividualInput(jTextField1, jLabel25) &&
                checkIndividualInput(jTextField4, jLabel24) &&
                checkIndividualInput(jTextField5, jLabel2) && 
                checkIndividualInput(jTextField8, jLabel34) && 
                checkIndividualInput(jTextField11, jLabel35) &&
                checkIndividualInput(jTextField10, jLabel36));
    }
    
    private boolean checkIndividualInput(javax.swing.JTextField field, javax.swing.JLabel label) {
        try {
            if (Double.parseDouble(field.getText()) < 0) {
                label.setText("Invalid!");
                return false;
            }
        } catch (Exception e) {
            label.setText("Invalid!");
            return false;
        }
        label.setText(" ");
        return true;
    }
    
    public static double roundAvoid(double value, int places) {
        double scale = Math.pow(10, places);
        return Math.round(value * scale) / scale;
    }
    
    private String[] getJoistNameArray() {
        String names[] = new String[1];
        try {
            Connection cn = this.db.getConnection();
            Statement stmt = null;
            String query = "SELECT name\n" +
                           "FROM JOIST_TABLE";
            stmt = cn.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            
            int size = 0;
            rs.last();    // moves cursor to the last row
            size = rs.getRow(); // get row id 
            names = new String[size];
            
            rs = stmt.executeQuery(query);
            
            int i = 0;
            while (rs.next()) {
                names[i] = rs.getString("name");
                i++;
            }
            return names;
            
        } catch (SQLException e ) {
            System.out.println(e.getMessage());
        }
        return names;
    }
    
    private void updateJoist() {
        String model = jComboBox1.getSelectedItem().toString();
        double span = Double.parseDouble(jTextField8.getText());
        double bearingLength = 0;
        if (Double.parseDouble(jComboBox2.getSelectedItem().toString()) == 1.75) {
            bearingLength = 1.75;
        } else if (Double.parseDouble(jComboBox2.getSelectedItem().toString()) == 3.5) {
            bearingLength = 3.5;
        }
        
        this.joist.setBearingLength(bearingLength);
        this.joist.setModel(model);
        this.joist.setSpan(span);
        
    }
    
    private double calculateDeflection() {
        double depth = this.joist.getDepth();
        double EI = this.joist.getEI();
        double span = this.joist.getSpan()/12;
        double w = Double.parseDouble(jTextField11.getText())/12;
        double deflection = 0;
        
        if (this.joist.getTJI_series().equals("560")) {
            deflection = (22.5 * w * Math.pow(span, 4))/EI + (2.29 * w * Math.pow(span, 2))/(depth * 100000);
        } else {
            deflection = (22.5 * w * Math.pow(span, 4))/EI + (2.67 * w * Math.pow(span, 2))/(depth * 100000);
        }
        return deflection;
    }
