    private void showResults() {
        boolean finalCheck = true;
        
        //MOMENT RESULT
        jLabel27.setText(this.joist.getMomentCapacity() + " ft-lb");
        double moment = Double.parseDouble(jTextField1.getText());
        if (moment <= this.joist.getMomentCapacity()) {
            jLabel16.setText("PASS");
            jLabel16.setForeground(Color.green);

        } else {
            jLabel16.setText("FAIL");
            jLabel16.setForeground(Color.red);
            finalCheck = false;
        }
        
        //SHEAR RESULT
        jLabel28.setText(this.joist.getShearCapacity() + " lb");
        double shear = Double.parseDouble(jTextField4.getText());
        if (shear <= this.joist.getShearCapacity()) {
            jLabel18.setText("PASS");
            jLabel18.setForeground(Color.green);

        } else {
            jLabel18.setText("FAIL");
            jLabel18.setForeground(Color.red);
            finalCheck = false;
        }
        
        //END SHEAR RESULT
        jLabel29.setText(this.joist.getEndShearCapacity() + " lb");
        double endShear = Double.parseDouble(jTextField5.getText());
        if (endShear <= this.joist.getEndShearCapacity()) {
            jLabel21.setText("PASS");
            jLabel21.setForeground(Color.green);

        } else {
            jLabel21.setText("FAIL");
            jLabel21.setForeground(Color.red);
            finalCheck = false;
        }
        
        //DEFLECTION RESULT
        double deflection = this.calculateDeflection();
        jLabel30.setText(roundAvoid(deflection, 3) + " in");
        double deflectionTolerance = Double.parseDouble(jTextField10.getText());
        if (deflection <= deflectionTolerance) {
            jLabel22.setText("PASS");
            jLabel22.setForeground(Color.green);

        } else {
            jLabel22.setText("FAIL");
            jLabel22.setForeground(Color.red);
            finalCheck = false;
        }
        
        if (finalCheck) {
            jLabel23.setText("JOIST SUCCESSFULLY MEETS CRITERIA!");
            jLabel23.setForeground(Color.green);
        } else {
            jLabel23.setText("JOIST DOES NOT MEET CRITERIA!");
            jLabel23.setForeground(Color.red);
        }
    }
    
